class Command(BaseCommand):
    help = '预置中国城市坐标数据'

    def handle(self, *args, **options):
        count = 0
        for name, full_name, province, lng, lat, city_type, pinyin in CITIES:
            _, created = CityCoordinate.objects.update_or_create(
                name=name,
                province=province,
                defaults={
                    'full_name': full_name,
                    'lng': lng,
                    'lat': lat,
                    'city_type': city_type,
                    'pinyin': pinyin,
                },
            )
            if created:
                count += 1
        total = CityCoordinate.objects.count()
        self.stdout.write(self.style.SUCCESS(
            f'新增 {count} 条城市坐标，共 {total} 条'
        ))
